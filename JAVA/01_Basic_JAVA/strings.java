public class strings {
    public static void main(String[] args) {
        String name = "Adityapandey";

        System.out.println(name);

        System.out.println(name.length()); // for string's length
        System.out.println(name.toLowerCase()); // for lower case
        System.out.println(name.toUpperCase()); // for upper case
        System.out.println(name.trim()); // for trim space
        System.out.println(name.substring(3, 7)); // for start and end with index
        System.out.println(name.replace('a', 'p')); //replace char

    }

}
